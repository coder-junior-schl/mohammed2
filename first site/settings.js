function applyTranslations(lang) {
  const t = translations[lang] || translations.en;
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key]) {
      el.textContent = t[key];
    }
  });
  // Update document direction for Arabic
  document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
  document.documentElement.lang = lang;
  
  // Add RTL class to body for additional styling
  if (lang === 'ar') {
    document.body.classList.add('rtl');
  } else {
    document.body.classList.remove('rtl');
  }
}

// ===== Load settings on page load =====
window.onload = function(){
  const darkMode = localStorage.getItem("darkMode")
  const contrast = localStorage.getItem("contrast")
  const textSize = localStorage.getItem("textSize")
  let lang = localStorage.getItem("lang") || "en"
  
  // Apply language on load
  applyTranslations(lang);
  
  if(darkMode==="on") enableDarkMode()
  if(contrast==="on") enableContrast()
  if(textSize) {
    document.body.style.fontSize = textSize;
  }

  updateDarkModeBtn()
  updateContrastBtn()
  updateTextSizeBtn()
  updateLangBtn()
}

// ===== Dark Mode =====
const darkModeBtn = document.getElementById('darkModeBtn')
if(darkModeBtn){
  darkModeBtn.onclick = function(){
    if(localStorage.getItem("darkMode")==="on"){
      localStorage.setItem("darkMode","off");
      disableDarkMode();
    }else{
      localStorage.setItem("darkMode","on");
      enableDarkMode();
    }
    updateDarkModeBtn();
  }
}

function enableDarkMode(){
  document.body.style.backgroundColor="#121212";
  document.body.style.color="white";
  document.querySelectorAll("a").forEach(a=>a.style.color="#9ad0ff");
}
function disableDarkMode(){
  document.body.style.backgroundColor="white";
  document.body.style.color="black";
  document.querySelectorAll("a").forEach(a=>a.style.color="black");
}
function updateDarkModeBtn(){
  if(darkModeBtn) darkModeBtn.innerText = (localStorage.getItem("darkMode")==="on") ? "ON" : "OFF";
}

// ===== High Contrast =====
const contrastBtn = document.getElementById('contrastBtn')
if(contrastBtn){
  contrastBtn.onclick = function(){
    if(localStorage.getItem("contrast")==="on"){
      localStorage.setItem("contrast","off")
      disableContrast();
    }else{
      localStorage.setItem("contrast","on")
      enableContrast();
    }
    updateContrastBtn();
  }
}

function enableContrast(){
  document.body.style.backgroundColor="black";
  document.body.style.color="yellow";
  document.querySelectorAll("a").forEach(a=>a.style.color="cyan");
}
function disableContrast(){
  document.body.style.backgroundColor="white";
  document.body.style.color="black";
  document.querySelectorAll("a").forEach(a=>a.style.color="black");
}
function updateContrastBtn(){
  if(contrastBtn) contrastBtn.innerText = (localStorage.getItem("contrast")==="on") ? "ON" : "OFF";
}

// ===== Text Size =====
const textSizeBtn = document.getElementById('textSizeBtn')
if(textSizeBtn){
  textSizeBtn.onclick = function(){
    if(localStorage.getItem("textSize")==="22px"){
      localStorage.setItem("textSize","16px");
      document.body.style.fontSize="16px";
    }else{
      localStorage.setItem("textSize","22px");
      document.body.style.fontSize="22px";
    }
    updateTextSizeBtn();
  }
}
function updateTextSizeBtn(){
  if(textSizeBtn) textSizeBtn.innerText = (localStorage.getItem("textSize")==="22px") ? "LARGE" : "MEDIUM";
}

// ===== Language =====
function updateLangBtn(){
  if(langBtn) {
    const lang = localStorage.getItem("lang") || "en";
    langBtn.innerText = lang === "ar" ? "AR" : "ENG";
  }
}

const langBtn = document.getElementById("langBtn")
if(langBtn){
  langBtn.onclick = function(){
    const currentLang = localStorage.getItem("lang") || "en";
    let newLang;
    
    if(currentLang === "en"){
      newLang = "ar";
      langBtn.innerText = "AR";
    } else {
      newLang = "en";
      langBtn.innerText = "ENG";
    }
    
    localStorage.setItem("lang", newLang);
    applyTranslations(newLang);
  }
}

